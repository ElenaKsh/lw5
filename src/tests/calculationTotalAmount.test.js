const calculationTotalAmount = require('../js/functions');

test('calculates the total amount of all products', () => {
  const productElements = [
    {
      id: 1,
      name: 'Молоко',
      count: 40,
      priceForOne: 50
    },
    {
      id: 2,
      name: 'Хлеб',
      count: 100,
      priceForOne: 20
    },
    {
      id: 3,
      name: 'Лук',
      count: 200,
      priceForOne: 5
    }
  ];
  expect(calculationTotalAmount.calculationTotalAmount(productElements)).toBe(5000);
});
